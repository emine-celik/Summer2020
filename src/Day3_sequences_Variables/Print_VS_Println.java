package Day3_sequences_Variables;

public class Print_VS_Println {
    public static void main(String[] args) {

        System.out.println("A");//Apends new line after printing (prefer
        System.out.println("B");
        System.out.println("C");

        System.out.println("=============");

        System.out.print("A");//A  //DOES NOT APPEND NEW LINE AFTER PRINTING
        System.out.print("B");//B
        System.out.print("C");//C



    }



}
