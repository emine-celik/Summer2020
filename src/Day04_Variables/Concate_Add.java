package Day04_Variables;

public class Concate_Add {
    public static void main(String[] args) {
          //addition: number + number
        System.out.println(12 + 13); //1213, 25 both of them number you can add
        System.out.println('A'+ 2); // A2,67 (char)both of them hase number
        System.out.println('A' + 'B'); // 131 ADDITION

        //Concatenation: append
        System.out.println("12" + 13);//1213 you CANT add because "12" (String) is TEXT not a number
        System.out.println("A" +2); // A2\
        System.out.println("Gender: " + "M"); //gender: M
        System.out.println("Tax: " +3.5);




    }






}
